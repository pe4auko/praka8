<?php
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // Отримання значень змінних $e і $f з форми
    $e = $_POST['e'];
    $f = $_POST['f'];

    // Виведення першої, потім другої
    echo "Перша змінна: " . $e . "<br>";
    echo "Друга змінна: " . $f . "<br>";

    // Виведення другої, потім першої
    echo "Друга змінна: " . $f . "<br>";
    echo "Перша змінна: " . $e . "<br>";
}
?>
